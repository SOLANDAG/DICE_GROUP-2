    private void unregisterClient() {
        int roomIndex = cbUnregister.getSelectedIndex();
        Room room = SEAMSystem.getInstance().getRooms().get(roomIndex);

        for (Client client : SEAMSystem.getInstance().getClients()) {
            if (client.getRoom() == room) {
                SEAMSystem.getInstance().unregisterClient(client);
                room.setOccupied(false);
                JOptionPane.showMessageDialog(this, "CLIENT UNREGISTERED SUCCESSFULLY.");
                new MainForm().setVisible(true);
                setVisible(false);
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "NO CLIENT FOUND FOR THE SELECTED ROOM.");
    }
    
