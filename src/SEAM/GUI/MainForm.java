    private void showClients() {
        StringBuilder clientsList = new StringBuilder();
        for (Client client : SEAMSystem.getInstance().getClients()) {
            clientsList.append("MKTRM00")
                        .append(client.getRoom().getNumber()).append("\t")
                        .append(client.getName()).append("\t")
                        .append(client.getIdNumber()).append("\t")
                        .append(client instanceof Student ? "STUDENT" : "EMPLOYEE").append("\t")
                        .append(client.getEquipment().getName()).append("\n" + "\n");
        }
        taClients.setText(clientsList.toString());
    }
    
